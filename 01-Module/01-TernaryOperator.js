// Ternary Operator
// The Ternary Operator is a Alternate way of writing an if-else statement.
// Sudo code of Ternary Operator
// condition ? expressionIfTrue : expressionIfFalse

// Code Example 1 
const age = 10;
const message = age >= 18 ? "You are an adult." : "You are a minor.";
console.log(message); // Output: You are a minor.

// Code Example 2
const userAge = 30;
const minAge = 18;
const displayMessage = userAge >= minAge ? 'You are an adult.' : 'You are a minor.';
console.log(displayMessage); // Output: You are an adult.
